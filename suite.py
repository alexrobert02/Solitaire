class Suite:
    def __init__(self, suite_name, suite_color):
        """
        Initialize a Suite object.

        Parameters:
        - suite_name: A string representing the name of the card suite.
        - suite_color: A tuple (R, G, B) representing the color of the card suite in RGB format.
        """
        self.name = suite_name
        self.color = suite_color
